            party really benefits. However, we also learned that small talk is used to find common ground that then
            enables the conversation to go deeper, but often the common ground is never found and the small talk is the
            entire conversation. This lead to begin thinking of solutions that could help participants in a conversation
            find something in common as to avoid extended amounts of small talk.
          </p>
